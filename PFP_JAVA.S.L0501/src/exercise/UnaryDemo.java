package exercise;

public class UnaryDemo {
	public static void main(String[] args) {
		int i=3;
		i++;
		
		System.out.println(i);
		++i;
		
		System.out.println(i);
		
		System.out.println(++i);
		
		System.out.println(i++);

		System.out.println(i);
	}
}
