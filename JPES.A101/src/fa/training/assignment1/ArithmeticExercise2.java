package fa.training.assignment1;

public class ArithmeticExercise2 {
	public static void main(String[] args) {
		int a=125;
		int b=24;
		System.out.println(a + " + "+b+ " = "+ (a+b));
		System.out.println(a + " - "+b+ " = "+ (a-b));
		System.out.println(a + " x "+b+ " = "+ (a*b));
		System.out.println(a + " / "+b+ " = "+ (a/b));
		System.out.println(a + " % "+b+ " = "+ (a%b));
	}
}
